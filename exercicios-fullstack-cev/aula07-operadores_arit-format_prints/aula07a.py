# Operadores Aritméticos:
"""
+ -> Adição             EX: 5 + 2 == 7
- -> Subtração          EX: 5 - 2 == 3
* -> Multiplicação      Ex: 5 * 2 == 10
/ -> Divisão            Ex: 5 / 2 == 2.5
** -> Potência          Ex: 5 ** 2 == 25
// -> Divisão inteira   Ex: 5 // 2 == 2
% -> Resto da divisão   Ex: 5 % 2 == 1
"""

# Ordem de precedência:
"""
1 -> ()
2 -> **
3 -> * / // %
4 -> + -
"""

# Funções de Operações:
"""
pow(4,3) == 4**3 -> Função de potência
"""
#OBS: Ao usar funções de operações se perde a ordem de precedência

# Raiz Quadrada:
"""
81**(1/2) == 9.0 -> Raiz Quadrada de 81
127**(1/3) == 5.026525695313479 -> Raiz Cúbica de 127
"""

# Operações com strings:
"""
'Oi'*5 -> 'OiOiOiOiOi'
'='*20 -> '===================='
"""